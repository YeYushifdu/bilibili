#算术运算符
print(1+1)    #加法运算
print(1-1)    #减法运算
print(2*4)    #乘法运算
print(1/2)    #除法运算
print(11//2)    #整除运算
print(11%2)    #取余运算
print(2**3)    #幂运算，表示2的3次方
#整除运算
print(9//4)    #2
print(-9//-4)    #2
#一正一负向下取整
print(9//-4)    #-3
print(-9//4)    #-3
#取余运算
#余数=被除数-除数*商
print(9%-4)    #9-(-4)*(-3)-->-3
print(-9%4)    #-9-4*(-3)-->3

